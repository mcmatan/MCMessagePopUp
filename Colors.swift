import Foundation
import UIKit

class Colors : NSObject {
    
    class func defaultBlue() -> UIColor {
        return UIColor(hexString: "1378bf")
    }
    
    class func lightBlue() -> UIColor {
        return UIColor(hexString: "48a1de")
    }
    
    class func lightGray() -> UIColor {
        return UIColor(hexString: "f3f3f3")
    }
    
    class func gray() -> UIColor {
        return UIColor(hexString:"c2c2c2")
    }
    
    class func darkGray() -> UIColor {
        return UIColor(hexString:"6d6e71")
    }
    
    class func deafultBlack() -> UIColor {
        return UIColor(hexString: "1b1b1c")
    }
    
    class func deafultOrange() -> UIColor {
        return UIColor(hexString: "fc451d")
    }

    class func deafultRed() -> UIColor {
        return UIColor(hexString: "f61717")
    }
    class func deafultGreen() -> UIColor {
        return UIColor(hexString: "43c43d")
    }
    class func creem() -> UIColor {
        return UIColor(hexString: "e4e0dd")
    }
    class func navBlack() -> UIColor {
        return UIColor(hexString: "1b1b1c")
    }
    
    class func navBlackTransparent() -> UIColor {
        return UIColor(hexString: "1b1b1c", alpha: 0.9)
    }
    
    
